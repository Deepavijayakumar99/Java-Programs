class Swapnumber
{
public static void main(String args[])
{
int firstno=10,secondno=20,temp;
System.out.println("before Swapping");
System.out.println("the value of first number:"+firstno);
System.out.println("the value of second number:"+secondno);
temp=firstno;
firstno=secondno;
secondno=temp;
System.out.println("after Swapping");
System.out.println("the value of first number:"+firstno);
System.out.println("the value of second number:"+secondno);
}
}