class switchcase
{
public static void main(String args[])
{
int number=10;
switch(number)
{
case 1:System.out.println("number one");
       break;
case 2:System.out.println("number 2");
       break;   
case 3:System.out.println("number 3");
       break;
default:
       System.out.println("invalid number..");
}
}
}